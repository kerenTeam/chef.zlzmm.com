    <img src="http://i3.meishichina.com/attachment/201206/4/454155_13388258022zyZ.jpg" style="margin: 0px; padding: 0px; border: 0px; vertical-align: middle; max-width: 650px;"/>
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    最近比较少有时间上来，一是家里有些事，二是姐妹们来家里玩，还有外甥们，难得大家都有空，所以这次相聚的时间比较长，快1个月了。每天一起起床、一起买菜、一起做饭，甚至一起洗衣服，简直就是形影不离。做不完的美食，聊不完的话，还有逛不完的街。再有空闲的时间，还打打牌，所以一天二十四小时实在是不够用。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    <img src="http://i3.meishichina.com/attachment/201206/4/454155_1338825788tZ69.jpg" style="margin: 0px; padding: 0px; border: 0px; vertical-align: middle; max-width: 650px;"/>
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    用小外甥的话说，这个是像“鸡米花”的“肉米花”，内馅是用新鲜的猪肉，加胡萝卜、香菇、鸡蛋、芝麻等做成的，很香很酥，孩子们很是喜欢，吃的时候三个孩子是一人一个分着吃的，分完了还得比比谁的大谁的小，生怕自己吃少了。小外甥还对我说，等他回家的时候，让我做上一包给他在路上吃。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    <img src="http://i3.meishichina.com/attachment/201206/4/454155_1338825761INfF.jpg" style="margin: 0px; padding: 0px; border: 0px; vertical-align: middle; max-width: 650px;"/>
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    -------------------------------------------------------香酥小丸子-----------------------------------------------
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    主料：肉糜500克
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    配料：鸡蛋、胡萝卜、干香菇、香葱、生姜、水淀粉、黑芝麻、面包糠
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    调料：大喜大烤肉酱、料酒、生抽
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    <img src="http://i3.meishichina.com/attachment/201206/4/454155_1338825720PuiZ.jpg" style="margin: 0px; padding: 0px; border: 0px; vertical-align: middle; max-width: 650px;"/>
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    1：主馅料：肉糜。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    2：配料：胡萝卜末、干香菇末、生姜末、葱白末。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    3：外皮用料：面包糠、黑芝麻。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    4：肉糜先加入适量大喜大烤肉酱、生抽、料酒拌匀，腌制15分钟左右出底味。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    <img src="http://i3.meishichina.com/attachment/201206/4/454155_1338825727wuw0.jpg" style="margin: 0px; padding: 0px; border: 0px; vertical-align: middle; max-width: 650px;"/>
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    5：腌制好的肉糜加入生姜末、葱白末。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    6：加入胡萝卜末和干香菇末拌匀。（干香菇需要事先浸泡好）
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    7：分次加入少许水淀粉搅拌至上劲。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    8：依次做成小肉丸，放在盘中。（做好的是2盘多）
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    <img src="http://i3.meishichina.com/attachment/201206/4/454155_1338825741Zkpj.jpg" style="margin: 0px; padding: 0px; border: 0px; vertical-align: middle; max-width: 650px;"/>
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    9：鸡蛋磕入碗中拌匀，肉丸先放入蛋液里裹上一层“浆”，再裹上一层面包糠。（事先把面包糠和黑芝麻拌匀）
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    10：依次裹好。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    11：锅中热油，油5分热时下入小丸子。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    12：炸至熟透，捞出沥油，并用吸油纸吸掉油份即可食用。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    <img src="http://i3.meishichina.com/attachment/201206/4/454155_1338825772n0H1.jpg" style="margin: 0px; padding: 0px; border: 0px; vertical-align: middle; max-width: 650px;"/>
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    <img src="http://i3.meishichina.com/attachment/201206/4/454155_1338825809Fc9f.jpg" style="margin: 0px; padding: 0px; border: 0px; vertical-align: middle; max-width: 650px;"/>
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    诗心片语：
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    1：如果没有大喜大烤肉酱腌肉，需要把肉按自己喜欢的口味调一下味。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    2：因为是裹面包糠的，所以丸子不适合过大（跟鸡米花一般大小即可），以免面包糠炸老了肉丸还没熟。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    3：面包糠里加入适量芝麻，香味更浓郁。
</p>
<p style="margin-top: 0px; margin-bottom: 1em; padding: 0px;">
    4：入锅时油温不可过高，以免面包糠炸焦。
</p>
